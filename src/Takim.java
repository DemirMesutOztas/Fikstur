public class Takim
{
    private String takimName;
    private int takimId;

    public Takim(String takimName,int takimId)
    {
        this.takimName = takimName;
        this.takimId = takimId;
    }

    public String getTakimName() {
        return takimName;
    }

    public void setTakimName(String takimName) {
        this.takimName = takimName;
    }

    public int getTakimId() {
        return takimId;
    }

    public void setTakimId(int takimId) {
        this.takimId = takimId;
    }


}
